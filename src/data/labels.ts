/**
 * 점수에 해당하는 "연애 vs 친구" 한줄 요약을 반환한다.
 * 구간별 여러 문구 중 랜덤으로 선택되며, 동일 score는 캐시되어 일관성을 유지한다.
 */
export function getLoveFriendLine(score: number): string {
  const cached = loveFriendCache.get(score);
  if (cached) return cached;

  const entry =
    LOVE_FRIEND_LINES.find((e) => score >= e.min) ??
    LOVE_FRIEND_LINES[LOVE_FRIEND_LINES.length - 1];
  const line = entry.lines[Math.floor(Math.random() * entry.lines.length)];
  loveFriendCache.set(score, line);
  return line;
}
